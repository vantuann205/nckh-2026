/**
 * Lookup tọa độ thật cho một vehicle record
 * Ưu tiên: street_coords table → Nominatim geocoding → fallback lat/lng từ data
 */
window.resolveStreetCoords = async function(street, district) {
  const key = `${street}|${district}`;

  // 1. Tra bảng tọa độ có sẵn
  if (window.STREET_COORDS[key]) {
    return window.STREET_COORDS[key];
  }

  // 2. Thử Nominatim geocoding (cache kết quả)
  const cacheKey = `geocode_${key}`;
  const cached = sessionStorage.getItem(cacheKey);
  if (cached) {
    try {
      const parsed = JSON.parse(cached);
      window.STREET_COORDS[key] = parsed; // cache vào memory
      return parsed;
    } catch(e) {}
  }

  try {
    const query = encodeURIComponent(`${street}, ${district}, Ho Chi Minh City, Vietnam`);
    const url = `https://nominatim.openstreetmap.org/search?q=${query}&format=json&limit=1&countrycodes=vn`;
    const res = await fetch(url, { headers: { 'Accept-Language': 'vi' } });
    const data = await res.json();

    if (data && data[0]) {
      const result = {
        lat: parseFloat(data[0].lat),
        lng: parseFloat(data[0].lon),
        polyline: null, // Nominatim không trả polyline
      };
      window.STREET_COORDS[key] = result;
      sessionStorage.setItem(cacheKey, JSON.stringify(result));
      return result;
    }
  } catch(e) {
    // Nominatim không khả dụng, dùng fallback
  }

  return null; // caller sẽ dùng lat/lng từ data gốc
};
